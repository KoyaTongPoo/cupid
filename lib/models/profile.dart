class Profile {
  final String userID;
  final String username;
  final String introduction;
  final bool isWomen;
  final int age;
  final bool isSeeker;

  Profile({
    required this.userID,
    required this.username,
    required this.introduction,
    required this.age,
    required this.isSeeker,
    required this.isWomen,
  });
}
